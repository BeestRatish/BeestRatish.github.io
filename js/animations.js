// Parallax effect
window.addEventListener('mousemove', (e) => {
    const x = (window.innerWidth - e.pageX * 2) / 100;
    const y = (window.innerHeight - e.pageY * 2) / 100;
    document.querySelector('.img-profile').style.transform = `translateY(${y}px) translateX(${x}px)`;
});

// Text animation
const textElements = document.querySelectorAll('h1, h2, h3, p, .subheading');
textElements.forEach(element => {
    element.innerHTML = element.textContent.split('').map((letter, idx) => 
        `<span style="transition-delay: ${idx * 30}ms">${letter}</span>`
    ).join('');
});

// Scroll reveal animation
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            // Animate skill bars
            if (entry.target.id === 'skills') {
                document.querySelectorAll('.skill-level').forEach(bar => {
                    const width = bar.getAttribute('data-level');
                    bar.style.width = width;
                });
            }
        }
    });
}, observerOptions);

document.querySelectorAll('.resume-section').forEach(section => {
    observer.observe(section);
});

// Add ripple effect to buttons
document.querySelectorAll('.btn').forEach(button => {
    button.addEventListener('click', function(e) {
        const x = e.clientX - e.target.getBoundingClientRect().left;
        const y = e.clientY - e.target.getBoundingClientRect().top;
        
        const ripple = document.createElement('span');
        ripple.classList.add('ripple');
        ripple.style.left = `${x}px`;
        ripple.style.top = `${y}px`;
        
        this.appendChild(ripple);
        
        setTimeout(() => {
            ripple.remove();
        }, 1000);
    });
});

// Add floating effect to elements with .float class
document.querySelectorAll('.float').forEach(element => {
    element.style.animation = `float ${3 + Math.random() * 3}s ease-in-out infinite`;
});

// Add typing effect to elements with .typing class
document.querySelectorAll('.typing').forEach(element => {
    const text = element.textContent;
    element.textContent = '';
    let i = 0;
    const speed = 50; // typing speed in milliseconds
    
    function typeWriter() {
        if (i < text.length) {
            element.textContent += text.charAt(i);
            i++;
            setTimeout(typeWriter, speed);
        }
    }
    
    setTimeout(typeWriter, 1000);
});
